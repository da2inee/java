/**
 * 2단계: 변수와 데이터 타입
 * 
 * int   - 정수
 * double - 실수
 * String - 문자열 (큰따옴표)
 * boolean - true / false
 */
public class Variables {
    public static void main(String[] args) {
        int age = 20;
        double height = 175.5;
        String name = "홍길동";
        boolean isStudent = true;

        System.out.println("이름: " + name);
        System.out.println("나이: " + age);
        System.out.println("키: " + height);
        System.out.println("학생? " + isStudent);
    }
}
